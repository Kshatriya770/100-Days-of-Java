package operators;

public class ComparisonOperators {

	public static void main(String[] args) {
		// they return boolean value
		int a = 3;
		int b = 7;
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a == b);
		System.out.println(a != b); //not equal to

	}

}
